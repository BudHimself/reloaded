int			ft_sqrt(int nb)
{
	int		x;
	int		sum;

	x = 0;
	sum = 0;
	if (nb > 0)
	{
		while (sum < nb)
		{
			x++;
			sum = x * x;
		}
		if (nb % x)
			return (0);
		else
			return (x);
	}
	else
		return (0);
}
